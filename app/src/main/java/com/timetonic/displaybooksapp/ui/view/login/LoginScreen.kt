@Composable
fun LoginScreen(
    loginViewModel: LoginViewModel,
    splashScreenViewModel: SplashScreenViewModel,
    navController: NavController
) {

    val apiKeyFromApi =
        splashScreenViewModel.apiKey.value.toString()

    loginViewModel.getApiKey(apiKeyFromApi)

    val effectLaunched = remember { mutableStateOf(false) }

    LaunchedEffect(true) {
        if (!effectLaunched.value) {
            loginViewModel.onCleanLoginSuccess()
            effectLaunched.value = true
        }
    }

    LaunchedEffect(Unit) {
        loginViewModel.onClearLoginFields()
    }

    Box(
        Modifier
            .fillMaxSize()
            .padding(8.dp)
    ) {
        Header(Modifier.align(Alignment.TopEnd))
        Body(Modifier.align(Alignment.Center), loginViewModel, navController)
        Footer(Modifier.align(Alignment.BottomCenter))
    }
}

@Composable
fun Body(modifier: Modifier, loginViewModel: LoginViewModel, navController: NavController) {

    val email: String by loginViewModel.email.observeAsState(initial = "")
    val password: String by loginViewModel.password.observeAsState(initial = "")
    val isLoginEnable: Boolean by loginViewModel.isLoginEnable.observeAsState(initial = false)

    Column(modifier = modifier) {
        //ImageLogo(Modifier.align(Alignment.CenterHorizontally))
        Spacer(modifier = Modifier.size(16.dp))
        Email(email) {
            loginViewModel.onLoginChanged(email = it, password = password)
        }
        Spacer(modifier = Modifier.size(4.dp))
        Password(password) {
            loginViewModel.onLoginChanged(email = email, password = it)
        }
        Spacer(modifier = Modifier.size(8.dp))
        ForgotPassword(Modifier.align(Alignment.End))
        Spacer(modifier = Modifier.size(16.dp))
        LoginButton(isLoginEnable, loginViewModel, navController)
        Spacer(modifier = Modifier.size(32.dp))
    }
}

@Composable
fun LoginButton(
    loginEnable: Boolean,
    loginViewModel: LoginViewModel,
    navController: NavController
) {

    val context = LocalContext.current

    val isLoginSuccess: Boolean by loginViewModel.isLoginSuccess.observeAsState(false)
    val errorMessage = loginViewModel.errorMessage.value

    var launchEffect by rememberSaveable { mutableStateOf(false) }

    Button(
        onClick = {
            loginViewModel.onCreateOAuthKey(
                loginViewModel.email.value.toString(),
                loginViewModel.password.value.toString(),
                loginViewModel.apiKey.value.toString()
            )
            launchEffect = true

        },
        enabled = loginEnable,
        modifier = Modifier
            .fillMaxWidth()
            .height(55.dp),
        colors = ButtonDefaults.buttonColors(
            containerColor = Color(0xFF4EA8E9),
            disabledContainerColor = Color(0xFF78C8F9),
            contentColor = Color.White,
            disabledContentColor = Color.White
        ),
    ) {
        Text(text = "Login")
    }


    if (launchEffect) {
        if (isLoginSuccess) {
            launchEffect = false
            LaunchedEffect(Unit) {
                navController.navigate(AppScreens.DescriptionBookScreen.route)
            }
        } else {
            LaunchedEffect(errorMessage) {
                errorMessage?.let { message ->
                    Toast.makeText(context, message, Toast.LENGTH_SHORT).show()
                    loginViewModel.onClearErrorMessage()
                }
            }
        }
    }
}

